package conditionalStatement;

public class SmallerOrGreater {

	public static void main(String[] args) {

		int number1=59;
		int number2=12;
		
		if(number1>number2) {
			System.out.println("Number  "+number1+"  is greater than "+number2);
		}else {
			System.out.println("Number  "+number1+"  is smaller than "+number2);

		}
	}

}
